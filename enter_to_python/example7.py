# Ввод и вывод данных

# input() - ввод данных
# print() - вывод данных

#int - работа с целыми числами
print('Введите 1 число')
a = int(input())
print('Введите 2 число')
b = int(input())
print(f'{a}+{b}={a+b}')

#float - работа с вещественными числами
print('Введите 1 число')
a = float(input())
print('Введите 2 число')
b = float(input())
print(f'{a}+{b}={a+b}')